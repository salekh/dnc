async def stream_recommender_generation(session_id: str, tweak: str = None) -> AsyncGenerator[str, None]:
    """Streams SSE events simulating Gemini 3.1 Pro generating the ES module."""
    specs = get_specs_data()
    total_tokens = get_total_tokens(specs)
    
    # Event 1: Assembling context
    status_msg = f"Assembling {total_tokens:,} tokens across {len(specs)} files → sending to Gemini 3.1 Pro"
    if tweak:
        status_msg += f" [Tweak: {tweak}]"
        
    yield f"data: {json.dumps({'type': 'status', 'message': status_msg, 'tokens': total_tokens})}\n\n"
    await asyncio.sleep(1.2)

    # Event 2: Streaming begins
    yield f"data: {json.dumps({'type': 'status', 'message': 'Streaming from gemini-3-1-pro ▮'})}\n\n"
    await asyncio.sleep(0.5)

    # Get target JS code
    full_js = generate_mock_js(tweak)

    # Stream character/line chunks
    lines = full_js.split("\n")
    for line in lines:
        chunk = line + "\n"
        yield f"data: {json.dumps({'type': 'chunk', 'text': chunk})}\n\n"
        await asyncio.sleep(0.08)

    # Store in session store
    session_modules[session_id] = full_js
    logger.info(f"Successfully generated and stored recommender module for session: {session_id}")

    # Event 3: Complete
    yield f"data: {json.dumps({'type': 'complete', 'path': '/demo/ruby/recommender.js', 'session_id': session_id, 'message': '✓ Written to /demo/ruby/recommender.js'})}\n\n"
