def get_specs_data() -> List[Dict[str, Any]]:
    """Reads the specification stack files and estimates token count."""
    specs = []
    for filename in SPEC_FILES:
        filepath = os.path.join(SPECS_DIR, filename)
        content = ""
        if os.path.exists(filepath):
            with open(filepath, "r", encoding="utf-8") as f:
                content = f.read()
        else:
            content = f"# {filename}\nSpecification file placeholder for Ruby TV."
        
        # Rough token estimate (~4 chars per token)
        tokens = max(1, len(content) // 4)
        specs.append({
            "name": filename,
            "path": filepath,
            "tokens": tokens,
            "content": content
        })
    return specs

def get_total_tokens(specs: List[Dict[str, Any]]) -> int:
    return sum(s["tokens"] for s in specs)

